/*
 * Leave Report
 * Write an expression that calculates the average number of leave days taken by the employees in this year. Replace your_answer with your expression.
 */

var annualLeave = 30
var BobRemainingDays = 12
var AliceRemainingDays = 28
var TomRemainingDays = 19
var average = (annualLeave - (BobRemainingDays+AliceRemainingDays+TomRemainingDays)) / 3

exports.average = average
exports.annualLeave = annualLeave
exports.BobRemainingDays = BobRemainingDays
exports.AliceRemainingDays = AliceRemainingDays
exports.TomRemainingDays = TomRemainingDays
